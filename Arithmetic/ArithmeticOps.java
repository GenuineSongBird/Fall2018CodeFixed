package Arithmetic;
public class ArithmeticOps {
	//making methods
	public int add(int num1, int num2) {
		int sum = num1 + num2;
		return sum;
	}
	//calculate the difference of two numbers
	//num1, num2- args/parameters
	public double diff(double num1, double num2) {
		double sub = num1 - num2;
		return sub;
	}
}
